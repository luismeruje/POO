
public class ViaturaNaoDisponivelException extends Exception {
	public ViaturaNaoDisponivelException(){	
		super();
	}
	public ViaturaNaoDisponivelException(String message){
		super(message);
	}
	public ViaturaNaoDisponivelException(Throwable cause){
		super(cause);
	}
	public ViaturaNaoDisponivelException(String message, Throwable cause){
		super(message, cause);
	}
	
}
